    public function save(){
        $data['uniacid'] = input("appletid");
        $data['title'] = input("title");
        $id = Db::name('wd_xcx_diy')->order("id desc")->limit(1)->select();
        $newid = 0;
        if(count($id)==0){
            $newid = 1;
        }else{
            $newid = count($id)+1;
        }   
        $data['url'] = "pages/main/main?pageid=".$newid;
        $data['desc'] = input("desc");
        $data['creattime'] = time();
        $res = Db::name('wd_xcx_diy')->insert($data);
        if($res){
           $this->success('页面新建成功！');
        }
    }
